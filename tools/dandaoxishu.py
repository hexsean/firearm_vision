def multiply_with_coefficient(data, coefficient):
    """将输入数据列表中的第二个元素乘以系数。

    Args:
        data: 输入数据，格式为 [x, y] 的列表的列表。
        coefficient: 系数。

    Returns:
        修改后的数据列表。
    """
    result = []
    for pair in data:
        x, y = pair  # 拆包列表元素
        result.append([x-1, round(y * coefficient)])  # 保留三位小数
    return result


